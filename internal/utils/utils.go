package utils

import (
	"encoding/json"
	"fmt"
	"reflect"
)

func trimQuote(s string) string {
	if len(s) > 0 && s[0] == '"' {
		s = s[1:]
	}
	if len(s) > 0 && s[len(s)-1] == '"' {
		s = s[:len(s)-1]
	}
	return s
}

func IsEqualJson(s1, s2 string) (bool, error) {
	var o1 interface{}
	var o2 interface{}

	var err error
	err = json.Unmarshal([]byte(s1), &o1)
	if err != nil {
		return false, fmt.Errorf("Error mashalling string 1 :: %s", err.Error())
	}
	err = json.Unmarshal([]byte(s2), &o2)
	if err != nil {
		return false, fmt.Errorf("Error mashalling string 2 :: %s", err.Error())
	}

	return reflect.DeepEqual(o1, o2), nil
}

func GetValueFromJson(jsonObject, key string) (string, error) {
	var err error

	mapJsonObject := make(map[string]json.RawMessage)
	if err = json.Unmarshal([]byte(jsonObject), &mapJsonObject); err != nil {
		return "", err
	}

	return trimQuote(string(mapJsonObject[key])), nil
}
